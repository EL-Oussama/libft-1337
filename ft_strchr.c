#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	lents;

	i = 0;
	lents = (int)ft_strlen(s);
	if ((char)c == '\0')
		return ((char *)&s[lents]);
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)&s[i]);
		i++;
	}
	return (NULL);
}
